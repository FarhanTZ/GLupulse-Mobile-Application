class Doctor {
  final String name;
  final String specialty;
  final String profileImage;
  final double rating;
  final String bio;
  final String location;
  final String addres;
  final String price;

  Doctor({
    required this.name,
    required this.specialty,
    required this.profileImage,
    required this.rating,
    required this.bio,
    required this.location,
    required this.addres,
    required this.price,
  });
}
